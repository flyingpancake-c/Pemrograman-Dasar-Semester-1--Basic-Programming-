#include <iostream>
#include <cstdio>
using namespace std;

int main() {
    // Manually setting the input values
    int i = 3;
    long l = 12345678912345;
    char c = 'a';
    float f = 334.23;
    double d = 14049.30493;
    
    // Printing output
    printf("%d\n", i);
    printf("%lld\n", l);
    printf("%c\n", c);
    printf("%.3f\n", f);
    printf("%.9lf\n", d);
    
    return 0;
}

